import sys
import base64
import json
import requests

# Путь к вашему изображению и текстовый запрос
IMAGE_PATH = sys.argv[1]
PROMPT = "Опишите, что находится на этом изображении."
OLLAMA_URL = "http://localhost:11434/api/generate"


def encode_image(image_path):
    with open(image_path, "rb") as image_file:
        return base64.b64encode(image_file.read()).decode("utf-8")


base64_image = encode_image(IMAGE_PATH)

payload = {
    "model": "gemma4:12b",
    "prompt": PROMPT,
    "images": [base64_image],
    "stream": True,  # Включаем поток для построчного чтения
}

try:
    # Отправляем запрос с параметром stream=True для requests
    response = requests.post(OLLAMA_URL, json=payload, stream=True)
    response.raise_for_status()

    print("Ответ модели: ", end="", flush=True)

    # Читаем ответ построчно по мере его генерации
    for line in response.iter_lines():
        if line:
            # Декодируем строку и превращаем каждый кусочек в JSON
            chunk = json.loads(line.decode("utf-8"))
            text_fragment = chunk.get("response", "")

            # Выводим текст в консоль в реальном времени
            print(text_fragment, end="", flush=True)

    print()  # Перенос строки в самом конце

except requests.exceptions.RequestException as e:
    print(f"\nОшибка подключения к Ollama: {e}")
except json.JSONDecodeError as e:
    print(f"\nОшибка обработки JSON: {e}")

